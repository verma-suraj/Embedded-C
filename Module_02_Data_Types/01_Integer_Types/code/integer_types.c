#include <stdio.h>

int main()
{
    char a = 10;
    unsigned char b = 255;

    short int c = -200;
    unsigned short int d = 500;

    int e = 1000;
    unsigned int f = 2000;

    printf("char a = %d\n", a);
    printf("unsigned char b = %u\n", b);
    printf("short c = %d\n", c);
    printf("unsigned short d = %u\n", d);
    printf("int e = %d\n", e);
    printf("unsigned int f = %u\n", f);

    return 0;
}
